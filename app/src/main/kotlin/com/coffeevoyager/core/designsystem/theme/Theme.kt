private val extendedColorsLight = ExtendedColors(
    imageBackground = imageBackgroundLight
)

private val extendedColorsDark = ExtendedColors(
    imageBackground = imageBackgroundDark
)


@Immutable
data class ExtendedColors(
    val imageBackground: Color
)

val LocalExtendedColors = staticCompositionLocalOf {
    ExtendedColors(
        imageBackground = Color.Unspecified
    )
}

@Composable
fun CoffeeTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val extendedColors = if (darkTheme) extendedColorsDark else extendedColorsLight
    CompositionLocalProvider(LocalExtendedColors provides extendedColors) {
        MaterialTheme(
            colorScheme = if (darkTheme) darkColorScheme else lightColorScheme,
            typography = Typography,
            content = content
        )
    }
}

object ExtendedTheme {
    val colors: ExtendedColors
        @Composable
        get() = LocalExtendedColors.current
}
